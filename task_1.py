import matplotlib.pyplot as plt
import numpy as np
   
x = [x for x in range(-6,6)]
y = [5 * x[i] * x[i] + 10 * x[i] - 30 for i in range(len(x))]
 
plt.plot(x,y, marker="o")   
plt.xticks(np.arange(-5, 5, step=1))   
plt.title("–ì—Ä–∞—Ñ–∏–∫ —Ñ—É–Ω–∫—Ü–∏–∏ f(x)=5*x*x+10*x-30") 
plt.xlabel('–û—Å—å x')    
plt.ylabel('–û—Å—å y') 
plt.show()   

